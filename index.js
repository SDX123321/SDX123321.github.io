var _searchIndex = null;
  var _fuse = null;
  var _searchLoading = false;

  function loadSearchIndex(){
    if(_searchIndex || _searchLoading) return;
    _searchLoading = true;
    fetch('search_index.json')
      .then(function(r){ return r.json(); })
      .then(function(data){
        _searchIndex = data;
        _fuse = new Fuse(data, {
          keys: [
            {name:'title', weight:0.5},
            {name:'context', weight:0.3},
            {name:'course', weight:0.2}
          ],
          threshold: 0.4,
          ignoreLocation: true,
          minMatchCharLength: 1,
          includeMatches: true
        });
        _searchLoading = false;
      })
      .catch(function(){ _searchLoading = false; });
  }

  function onSearch(query){
    var box = document.getElementById('searchResults');
    if(!query || query.length < 1){ box.style.display='none'; return; }
    loadSearchIndex();
    if(!_fuse){ box.innerHTML='<div style="padding:16px;color:var(--text3);font-size:.85rem;">加载中...</div>'; box.style.display='block'; return; }

    var results = _fuse.search(query).slice(0, 20);
    if(results.length === 0){
      box.innerHTML='<div style="padding:16px;color:var(--text3);font-size:.85rem;text-align:center;">未找到相关内容</div>';
      box.style.display='block';
      return;
    }

    var html = results.map(function(r){
      var item = r.item;
      var typeBadge = item.type === '资料' || item.type === '电子课本'
        ? '<span style="display:inline-block;padding:1px 6px;border-radius:4px;background:rgba(245,158,11,.15);color:#d97706;font-size:.7rem;font-weight:600;">'+item.type+'</span>'
        : '<span style="display:inline-block;padding:1px 6px;border-radius:4px;background:rgba(37,99,235,.1);color:#2563eb;font-size:.7rem;font-weight:600;">'+item.course+'</span>';
      var context = item.context ? '<div style="font-size:.78rem;color:var(--text3);margin-top:2px;overflow:hidden;text-overflow:ellipsis;white-space:nowrap;">'+item.context.substring(0,80)+'</div>' : '';
      return '<a href="'+item.url+'" style="display:block;padding:10px 16px;border-bottom:1px solid var(--border);text-decoration:none;color:var(--text);transition:background .15s;" onmouseover="this.style.background=\'rgba(128,128,128,.06)\'" onmouseout="this.style.background=\'transparent\'">'
        + typeBadge + ' '
        + '<span style="font-size:.88rem;font-weight:500;">'+item.title+'</span>'
        + context + '</a>';
    }).join('');

    box.innerHTML = html;
    box.style.display = 'block';
  }

