// ==================== BROWSE ====================
function renderBrowse(){
  if(!S.chapter) return;
  const ch=S.chapter;
  const total=ch.sections.reduce((s,sec)=>s+sec.kp.length,0);
  const known=ch.sections.reduce((s,sec)=>s+sec.kp.filter(kp=>S.known[kp.q]===true).length,0);
  let h=`<div class="fade-in">
    <div class="page-title">${ch.icon} ${ch.title}</div>
    <div class="page-sub">共 ${total} 个知识点，已掌握 ${known} 个</div>
    <div class="stats">
      <div class="stat"><div class="num">${total}</div><div class="lbl">知识点</div></div>
      <div class="stat ok"><div class="num">${known}</div><div class="lbl">已掌握</div></div>
      <div class="stat warn"><div class="num">${total-known}</div><div class="lbl">待复习</div></div>
    </div>`;
  ch.sections.forEach((sec,si)=>{
    const isOpen=S.openSec.has(si);
    h+=`<div class="section">
      <div class="sec-head ${isOpen?'open':''}" onclick="toggleSec(${si})">
        <h3>${sec.title}</h3><span class="badge">${sec.kp.length} 项</span><span class="arrow">▸</span>
      </div><div class="qa-list" style="display:${isOpen?'flex':'none'}">`;
    sec.kp.forEach((kp,ki)=>{
      const id=ch.id+'_'+si+'_'+ki;
      const isOpen2=S.openCards.has(id);
      const status=S.known[kp.q]===true?'known':S.known[kp.q]===false?'unknown':'';
      const tHtml=kp.t.map(t=>`<span class="tag tag-${t}">${t}</span>`).join('');
      h+=`<div class="qa ${isOpen2?'open':''} ${status}" id="qa-${id}">
        <div class="qa-q" onclick="toggleCard('${id}')">
          <div class="qi">Q</div><div class="qt">${kp.q}</div><div class="ti">▼</div>
        </div><div class="qa-a"><div class="qa-a-inner">${kp.a}</div>
          <div class="qa-acts">${tHtml}<span style="flex:1"></span>
            <button class="kb ${S.known[kp.q]===true?'active':''}" onclick="event.stopPropagation();markKnown('${esc(kp.q)}',true)">✓ 已掌握</button>
            <button class="ub ${S.known[kp.q]===false?'active':''}" onclick="event.stopPropagation();markKnown('${esc(kp.q)}',false)">✗ 待复习</button>
          </div></div></div>`;
    });
    h+='</div></div>';
  });
  h+='</div>';
  document.getElementById('content').innerHTML=h;
}
function toggleSec(i){
  S.openSec.has(i)?S.openSec.delete(i):S.openSec.add(i);
  var secs=document.querySelectorAll('.section');
  if(secs[i]){
    var head=secs[i].querySelector('.sec-head');
    var list=secs[i].querySelector('.qa-list');
    if(head&&list){
      var isOpen=S.openSec.has(i);
      head.classList.toggle('open',isOpen);
      list.style.display=isOpen?'flex':'none';
    }
  }
  localStorage.setItem('mk',JSON.stringify(S.known));
}
function toggleCard(id){
  S.openCards.has(id)?S.openCards.delete(id):S.openCards.add(id);
  var el=document.getElementById('qa-'+id);
  if(el) el.classList.toggle('open',S.openCards.has(id));
  localStorage.setItem('mk',JSON.stringify(S.known));
}
function esc(q){return q.replace(/'/g,"\\'").replace(/"/g,'&quot;')}
function markKnown(q,v){
  q=q.replace(/\\'/g,"'").replace(/&quot;/g,'"');
  if(S.known[q]===v) delete S.known[q]; else S.known[q]=v;
  localStorage.setItem('mk',JSON.stringify(S.known));
  // Update card class directly without full re-render
  document.querySelectorAll('.qa').forEach(function(card){
    var qt=card.querySelector('.qt');
    if(!qt) return;
    var qText=qt.textContent;
    var status=S.known[qText]===true?'known':S.known[qText]===false?'unknown':'';
    card.classList.remove('known','unknown');
    if(status) card.classList.add(status);
  });
}

// ==================== FLASHCARD ====================
function initFC(){
  if(!S.chapter) return;
  S.fcPool=[]; S.chapter.sections.forEach(sec=>sec.kp.forEach(kp=>S.fcPool.push(kp)));
  S.fcIdx=0; S.fcFlipped=false; renderFC();
}
function renderFC(){
  if(!S.fcPool.length) return;
  const kp=S.fcPool[S.fcIdx];
  const pct=Math.round((S.fcIdx+1)/S.fcPool.length*100);
  document.getElementById('content').innerHTML=`
    <div class="fc-wrap fade-in">
      <div class="page-title">${S.chapter.icon} ${S.chapter.title} · 闪卡</div>
      <div class="fc-bar"><div class="prog"><div class="prog-fill" style="width:${pct}%"></div></div><div class="cnt">${S.fcIdx+1} / ${S.fcPool.length}</div></div>
      <div class="fc ${S.fcFlipped?'flipped':''}" onclick="flipFC()">
        <div class="fc-inner">
          <div class="fc-face fc-front"><h3>问题</h3><p>${kp.q}</p><div class="hint">点击翻转查看答案 · 空格键</div></div>
          <div class="fc-face fc-back"><h3>答案</h3><p>${kp.a}</p><div style="margin-top:12px">${kp.t.map(t=>`<span class="tag tag-${t}">${t}</span>`).join(' ')}</div></div>
        </div>
      </div>
      <div class="fc-nav">
        <button onclick="event.stopPropagation();prevFC()">← 上一张</button>
        <button class="ng" onclick="event.stopPropagation();markFC(false)">✗ 待复习</button>
        <button class="ok" onclick="event.stopPropagation();markFC(true)">✓ 已掌握</button>
        <button class="pri" onclick="event.stopPropagation();nextFC()">下一张 →</button>
      </div>
    </div>`;
}
function flipFC(){S.fcFlipped=!S.fcFlipped;renderFC()}
function nextFC(){if(S.fcIdx<S.fcPool.length-1){S.fcIdx++;S.fcFlipped=false;renderFC()}}
function prevFC(){if(S.fcIdx>0){S.fcIdx--;S.fcFlipped=false;renderFC()}}
function markFC(v){const kp=S.fcPool[S.fcIdx];S.known[kp.q]=v;localStorage.setItem('mk',JSON.stringify(S.known));nextFC()}

