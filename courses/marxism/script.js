function showSearch(q){
  const results=[];
  DATA.forEach(ch=>ch.sections.forEach(sec=>sec.kp.forEach(kp=>{
    if(kp.q.includes(q)||kp.a.replace(/<[^>]*>/g,'').includes(q)) results.push({ch,kp});
  })));
  let h=`<div class="fade-in"><div class="page-title">搜索：${q}</div><div class="page-sub">找到 ${results.length} 个结果</div>`;
  if(!results.length) h+='<p style="color:var(--text2);text-align:center;padding:40px">未找到匹配内容</p>';
  results.forEach(r=>{
    h+=`<div class="sr" onclick="goChapter('${r.ch.id}')"><h4>${r.ch.icon} ${r.ch.title} · ${r.kp.q}</h4><p>${r.kp.a.replace(/<[^>]*>/g,'').substring(0,120)}…</p></div>`;
  });
  h+='</div>';
  document.getElementById('content').innerHTML=h;
}

