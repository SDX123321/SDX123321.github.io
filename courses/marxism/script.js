// ==================== INIT ====================
function init(){
  // Show update guide for returning users
  const v = localStorage.getItem('marx_update_v2');
  const sg = localStorage.getItem('sg_done_marx');
  if(!v) document.getElementById('updateGuide').style.display='flex';
  else if(!sg) document.getElementById('subGuide').style.display='flex';

  buildNav();
  showWelcome();
  bindEvents();
}

function buildNav(){
  const nav=document.getElementById('nav');
  let h='<div class="nav-group"><div class="nav-title">目录</div>';
  h+=`<div class="nav-item" data-ch="welcome" onclick="goChapter(null)"><span class="icon">🏠</span><span>首页</span></div>`;
  DATA.forEach(ch=>{
    const cnt=ch.sections.reduce((s,sec)=>s+sec.kp.length,0);
    h+=`<div class="nav-item" data-ch="${ch.id}" onclick="goChapter('${ch.id}')"><span class="icon">${ch.icon}</span><span>${ch.title}</span><span class="count">${cnt}</span></div>`;
  });
  h+='</div>';
  nav.innerHTML=h;
}

function bindEvents(){
  document.getElementById('searchInput').addEventListener('input',e=>{
    const q=e.target.value.trim(); S.searchQ=q;
    if(q.length>=1) showSearch(q);
    else if(S.chapter) renderBrowse(); else showWelcome();
  });
  // Menu button
  const menuBtn=document.getElementById('menuBtn');
  if(menuBtn) menuBtn.addEventListener('click',()=>document.getElementById('sidebar').classList.toggle('open'));
  // Overlay
  const overlay=document.getElementById('overlay');
  if(overlay) overlay.addEventListener('click',()=>document.getElementById('sidebar').classList.remove('open'));
  // Keyboard
  document.addEventListener('keydown',e=>{
    if(e.target.tagName==='INPUT')return;
    if(S.mode==='flashcard'){
      if(e.key===' '||e.key==='Enter'){e.preventDefault();flipFC()}
      if(e.key==='ArrowRight') nextFC();
      if(e.key==='ArrowLeft') prevFC();
    }
  });
}

// ==================== NAV ====================
function goChapter(id){
  S.chapter=id?DATA.find(c=>c.id===id):null;
  S.openSec.clear();S.openCards.clear();S.searchQ='';
  document.getElementById('searchInput').value='';
  document.querySelectorAll('.nav-item').forEach(n=>{
    n.classList.toggle('active',id?n.dataset.ch===id:n.dataset.ch==='welcome');
  });
  if(S.chapter){
    S.chapter.sections.forEach((_,i)=>S.openSec.add(i));
    setMode('browse'); renderBrowse();
  }else{ showWelcome(); }
  document.getElementById('sidebar').classList.remove('open');
}
function setMode(m){
  S.mode=m;
  document.querySelectorAll('.mode-tab').forEach(b=>b.classList.toggle('active',b.dataset.mode===m));
  switch(m){
    case 'browse': if(S.chapter) renderBrowse(); break;
    case 'flashcard': if(S.chapter) initFC(); break;
    case 'quiz': initQuiz(); break;
    case 'stats': renderStats(); break;
  }
}

